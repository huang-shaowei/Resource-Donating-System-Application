    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)medicalcampHospitalTbl.getModel();
       
        model.setRowCount(0);
        
        for (WorkRequest request : userAccount.getWorkQueue().getWorkRequestList()){
        Object[] row = new Object[4];
      
        row[0] = request.getRequestorName();
        row[1] = request.getStatus();
        row[2] = request.getDetails();
        String response = ((MedicalCampWorkRequest) request).getLocation();
        row[3] = response == null ? "Waiting" : response;
            model.addRow(row);
        }
        
}
