    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:

        String details = requirementTxtArea.getText();
        
        MedicalCampWorkRequest req = new MedicalCampWorkRequest();
        
        req.setRequestorName(userAccount);
        req.setDetails(details);
        req.setStatus("Awaiting response");
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof CommunityCenterOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(req);
            
            userAccount.getWorkQueue().getWorkRequestList().add(req);          
        }
  
        populateTable();
    }//GEN-LAST:event_saveBtnActionPerformed

